REM     CREATION ON ALL THE TABLES


CREATE TABLE CUSTOMERS(
 CUSTOMER_ID	number(18)   NOT NULL,
 COMPANY	VARCHAR2(50),
 FIRST_NAME     VARCHAR2(30) NOT NULL,
 LAST_NAME	VARCHAR2(30) NOT NULL,
 ADDRESS1	VARCHAR2(72)	NOT NULL,
 ADDRESS2	VARCHAR2(72),
 CITY	VARCHAR2(30)	NOT NULL,
 STATE	VARCHAR2(2)	NOT NULL,
 POSTAL_CODE	VARCHAR2(10)		NOT NULL,
 PHONE	VARCHAR2(30)	NOT NULL,
 EMAIL	VARCHAR2(30)
);

ALTER TABLE CUSTOMERS 
ADD PRIMARY KEY (CUSTOMER_ID);

CREATE TABLE DEPARTMENT (
 DEPTID		Number(2) NOT NULL,
 DEPTNAME	VARCHAR2(30)
);

ALTER TABLE DEPARTMENT 
ADD PRIMARY KEY (DEPTID);

CREATE TABLE DISTRIBUTORS (
 DISTRIBUTOR_ID	INT	NOT NULL,
 DISTRIBUTOR_NAME	VARCHAR2(48)	NOT NULL,
 CONTACT_PERSON	VARCHAR2(48),
 CONTACT_PHONE	VARCHAR2(32),
 CONTACT_EMAIL	VARCHAR2(72)
);

ALTER TABLE DISTRIBUTORS 
ADD PRIMARY KEY (DISTRIBUTOR_ID);

CREATE TABLE MANUFACTURERS(
 MANUFACTURER_ID	INT	NOT NULL,
 MANUFACTURER_NAME	VARCHAR2(48)	NOT NULL
);

ALTER TABLE MANUFACTURERS
ADD PRIMARY KEY(MANUFACTURER_ID);

create table employees
(
Employee_id number(2) constraint emp_pk_id primary key,
DeptID number(2) references department(deptid),
Lastname varchar2(30) not null,
Firstname varchar2(30) not null,
Address varchar2(80) not null,
City varchar2(40) not null,
Postal_code varchar2(10) not null,
Country varchar2(10) not null,
Homephone varchar2(20) not null
);


CREATE TABLE STORES (
 STORE_ID	INT	NOT NULL,
 STORE_DESC	VARCHAR2(72)	NOT NULL,
 ADDRESS1	VARCHAR2(72)	NOT NULL,
 ADDRESS2	VARCHAR2(72),
 CITY	VARCHAR2(32)	NOT NULL,
 STATE	VARCHAR2(2)	NOT NULL,
 POSTAL_CODE	VARCHAR2(10)	NOT NULL,
 MANAGER_ID	INT	NOT NULL,
 PHONE	VARCHAR2(32)	NOT NULL
);

ALTER TABLE STORES
ADD PRIMARY KEY (STORE_ID);

ALTER TABLE STORES
ADD FOREIGN KEY (MANAGER_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID);

CREATE TABLE ITEMS (
 ITEM_ID	INT	NOT NULL,
 ITEM_NAME	VARCHAR2(72)	NOT NULL,
 ITEM_DESC	VARCHAR2(72),
 PRICE	NUMBER(10,2)	NOT NULL,
 WHOLESALE_COST	NUMBER(10,2)	NOT NULL,
 DISCONTINUED_FLAG	INT,
 MANUFACTURER_ID	INT,
 DISTRIBUTOR_ID	INT
);

ALTER TABLE ITEMS
ADD PRIMARY KEY (ITEM_ID);

ALTER TABLE ITEMS
ADD FOREIGN KEY (MANUFACTURER_ID) REFERENCES MANUFACTURERS(MANUFACTURER_ID);

ALTER TABLE ITEMS
ADD FOREIGN KEY (DISTRIBUTOR_ID) REFERENCES DISTRIBUTORS(DISTRIBUTOR_ID);

CREATE TABLE PROMOTIONS (
 PROMOTION_ID	INT	NOT NULL,
 PROMOTION_NAME	VARCHAR2(72)	NOT NULL,
 DESCRIPTION	VARCHAR2(72),
 START_DATE	DATE	NOT NULL,
 END_DATE	DATE	NOT NULL
);

ALTER TABLE PROMOTIONS
ADD PRIMARY KEY (PROMOTION_ID);

CREATE TABLE ITEMS_IN_PROMOTIONS (
 PROMOTION_ID	INT	NOT NULL,
 ITEM_ID	INT	NOT NULL,
 COMMENTS	VARCHAR2(72),
 DISCOUNT	NUMBER(10,2)
);

ALTER TABLE ITEMS_IN_PROMOTIONS
ADD PRIMARY KEY (PROMOTION_ID, ITEM_ID);

ALTER TABLE ITEMS_IN_PROMOTIONS
ADD FOREIGN KEY (ITEM_ID) REFERENCES ITEMS(ITEM_ID);

ALTER TABLE ITEMS_IN_PROMOTIONS
ADD FOREIGN KEY (PROMOTION_ID) REFERENCES PROMOTIONS (PROMOTION_ID);

CREATE TABLE ORDERS (
 ORDER_ID	INT	NOT NULL,
 DATE_ENTERED	DATE	NOT NULL,
 DATE_PROMISED	DATE	NOT NULL,
 DATE_SHIPPED	DATE,
 EMPLOYEE_ID	INT,
 CUSTOMER_ID	INT,
 SALES_TAX_RATE	NUMBER(5,4)	NOT NULL,
 STORE_ID	INT
)
;

ALTER TABLE ORDERS
ADD PRIMARY KEY(ORDER_ID);

ALTER TABLE ORDERS
ADD FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID);

ALTER TABLE ORDERS
ADD FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID);

ALTER TABLE ORDERS
ADD FOREIGN KEY (STORE_ID) REFERENCES STORES(STORE_ID);

CREATE TABLE ORDER_ITEMS (
 ORDER_ID	INT	NOT NULL,
 ITEM_ID	INT	NOT NULL,
 QUANTITY	INT	NOT NULL,
 DISCOUNT	NUMBER(10,2)	NOT NULL
);


ALTER TABLE ORDER_ITEMS
ADD PRIMARY KEY(ORDER_ID, ITEM_ID);

ALTER TABLE ORDER_ITEMS
ADD FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID);

ALTER TABLE ORDER_ITEMS
ADD FOREIGN KEY (ITEM_ID) REFERENCES ITEMS(ITEM_ID);

COMMIT;


